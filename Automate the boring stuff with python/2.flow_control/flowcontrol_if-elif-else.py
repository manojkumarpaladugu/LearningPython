# example on if condition
name = 'Manoj'
if name == 'Manoj':
    print('Matching names')

# example on if-else condition
password = 'hello123'
if password == 'hello123':
    print('Access granted')
else:
    print('Access not granted')

# example on if-elif-else
rank = 1
if rank == 1:
    print('You won 1st rank')
elif rank == 2:
    print('You won 2nd rank')
else:
    print('You lost')

# non empty string is TRUE
print(bool('Hello'))

# empty string is FALSE
print(bool(''))
